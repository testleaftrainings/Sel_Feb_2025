package week1.day2;

public class LearnforLoop {

	public static void main(String[] args) {
		
		//Print numbers from 1 to 5
		for(int a=0;a<=5;a++) {
			System.out.println(a);
		}
		
		System.out.println("-------------");
		
		//Print numbers from 5 to 1
		  for (int i = 5; i >=1; i--) {
			System.out.println(i);
		}
    }

}
